-- | Verify that a URL resolves to a 200 response.
--
-- Issues a GET request and returns 'True' only when the HTTP status code is
-- 200.  Any network error or non-200 status yields 'False', so a single
-- broken URL does not abort the whole discovery batch.
checkUrl :: Manager -> Text -> IO Bool
checkUrl mgr url = do
  result <- (try :: IO Int -> IO (Either SomeException Int)) $ do
    req <- parseRequest (T.unpack url)
    resp <- httpLbs req mgr
    pure $ statusCode (responseStatus resp)
  pure $ case result of
    Right 200 -> True
    _ -> False

-- | Ask Gemini (with Google Search grounding) to discover recent, relevant
--   content and return it as a structured list.
--
-- The @subjects@ list is read from the database by the caller and substituted
-- into the prompt template at runtime, so the search scope always reflects
-- what is in the Subject table rather than being hardcoded.
--
-- The model provides redirect URLs for each item.  These are followed
-- immediately (while still valid) via 'followRedirect' to obtain the real,
-- permanent source URLs.  The HTML body of each resolved URL is then fetched
-- generator.
discoverContent :: AiConfig -> [Text] -> IO (Int, [DiscoveredContent])
discoverContent cfg subjects = do
  (txt, tokensUsed) <- callGemini cfg requestBody'
  items <- decodeText (stripFences txt) :: IO [DiscoveredContent]
  results <- mapM (resolveItem (aiManager cfg)) items
  pure (tokensUsed, catMaybes results)
  where
    bullets = T.unlines ["  - " <> s | s <- subjects]
    names = T.intercalate ", " (map (\s -> "\"" <> s <> "\"") subjects)
    prompt =
      T.replace "{{SUBJECTS}}" bullets $
        T.replace "{{SUBJECT_NAMES}}" names discoverPrompt
    resolveItem mgr dc = do
      realUrl <- followRedirect mgr (dcUrl dc)
      -- Discard items whose URL is still on the vertexaisearch proxy domain
      -- (redirect failed) or whose resolved URL returns a non-200 status.
      if "vertexaisearch" `T.isInfixOf` realUrl
        then pure Nothing
        else do
          ok <- checkUrl mgr realUrl
          pure $ if ok then Just dc {dcUrl = realUrl} else Nothing
    -- google_search grounding is incompatible with responseMimeType/responseSchema
    -- JSON mode; the prompt instructs the model to return JSON directly.
    requestBody' =
      object
        [ "contents" .= [userContents prompt],
          "tools" .= [object ["google_search" .= object []]]
        ]

-- | Ask Gemini to write a full Markdown blog post draft based on the provided
--   content sources.  Returns both an English and a Brazilian Portuguese version.
generateDraft :: AiConfig -> [DiscoveredContent] -> IO GeneratedDraft
generateDraft cfg sources = do
  (mdText, tokensUsed) <- callGemini cfg requestBody'
  pure (assembleGeneratedDraft tokensUsed mdText)
  where
    sourcesBlock = T.unlines $ zipWith formatSource ([1 ..] :: [Int]) sources
    formatSource i dc =
      T.pack (show i)
        <> ". **"
        <> dcTitle dc
        <> "**\n   URL: "
        <> dcUrl dc
        <> "\n   "
        <> dcSummary dc
    prompt = T.replace "{{SOURCES}}" sourcesBlock draftPromptTemplate
    requestBody' =
      object
        [ "contents" .= [userContents prompt],
          "generationConfig" .= defaultGenerationConfig
        ]

-- | Input to 'reviseDraft'.
data ReviseRequest = ReviseRequest
  { -- | Current English Markdown body.
    rvBodyEn :: !Text,
    -- | Current Brazilian Portuguese Markdown body.
    rvBodyPtBr :: !Text,
    -- | Reviewer feedback to pass to the model.
    rvFeedback :: !Text
  }

-- | Ask Gemini to revise an existing bilingual draft based on reviewer feedback.
--
-- The returned 'GeneratedDraft' has the same 'gdBranch' derived from the
-- (possibly updated) title in the revised English content.
reviseDraft :: AiConfig -> ReviseRequest -> IO GeneratedDraft
reviseDraft cfg ReviseRequest {..} = do
  (mdText, tokensUsed) <- callGemini cfg requestBody'
  pure (assembleGeneratedDraft tokensUsed mdText)
  where
    prompt =
      T.replace "{{DRAFT_EN}}" rvBodyEn $
        T.replace "{{DRAFT_PTBR}}" rvBodyPtBr $
          T.replace "{{FEEDBACK}}" rvFeedback revisePromptTemplate
    requestBody' =
      object
        [ "contents" .= [userContents prompt],
          "generationConfig" .= defaultGenerationConfig
        ]

-- | Ask Gemini to write a bilingual blog post based on custom author instructions.
--
-- This is used for on-demand posts initiated from a Discord forum thread:
-- the thread title becomes the @titleHint@ and the first message becomes
-- @instructions@.  The AI may refine the title.  No source content is needed.
generateCustomDraft :: AiConfig -> Text -> Text -> IO GeneratedDraft
generateCustomDraft cfg titleHint instructions = do
  (mdText, tokensUsed) <- callGemini cfg requestBody'
  pure (assembleGeneratedDraft tokensUsed mdText)
  where
    prompt =
      T.replace "{{TITLE_HINT}}" titleHint $
        T.replace "{{INSTRUCTIONS}}" instructions customPostPromptTemplate
    requestBody' =
      object
        [ "contents" .= [userContents prompt],
          "generationConfig" .= defaultGenerationConfig
        ]

