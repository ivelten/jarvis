module Orchestrator.AI.Client
  ( AiConfig (..),
    DiscoveredContent (..),
    GeneratedDraft (..),
    discoverContent,
    generateDraft,
  )
where

import Data.Aeson (FromJSON, ToJSON)
import Data.Text (Text)
import GHC.Generics (Generic)
import Network.HTTP.Client (Manager)

-- | Runtime configuration for the GPT client.
data AiConfig = AiConfig
  { -- | OpenAI API key (from environment).
    aiApiKey :: !Text,
    -- | Model identifier, e.g. @"gpt-4o"@.
    aiModel :: !Text,
    aiManager :: !Manager
  }

-- | A piece of web content discovered by the AI.
data DiscoveredContent = DiscoveredContent
  { dcTitle :: !Text,
    dcUrl :: !Text,
    dcSummary :: !Text,
    dcSubject :: !(Maybe Text) -- subject name hint; matched against the Subject table at ingest time
  }
  deriving (Show, Eq, Generic)

instance FromJSON DiscoveredContent

instance ToJSON DiscoveredContent

