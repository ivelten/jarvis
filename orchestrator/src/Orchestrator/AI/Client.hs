-- | Runtime configuration for the Gemini client.
data AiConfig = AiConfig
  { -- | Google AI API key (from environment, see https://aistudio.google.com/apikey).
    aiApiKey :: !Text,
    -- | Ordered list of model identifiers to try, highest-priority first.
    -- When a model returns a rate-limit error (HTTP 429 / RESOURCE_EXHAUSTED)
    -- the next model in the list is attempted automatically.
    aiModels :: ![Text],
    aiManager :: !Manager
  }

-- | A piece of web content discovered by the AI.
data DiscoveredContent = DiscoveredContent
  { dcTitle :: !Text,
    dcUrl :: !Text,
    dcSummary :: !Text,
    -- | Subject name hints; each is matched against the Subject table at ingest time.
    dcSubjects :: ![Text]
  }
  deriving (Show, Eq, Generic)

-- | Raw model output from the discovery prompt (no URL — URLs come from
-- grounding chunks via 'mergeWithChunks').
data ModelItem = ModelItem
  { miTitle :: !Text,
    miSummary :: !Text,
    miSubjects :: ![Text]
  }
  deriving (Show, Eq, Generic)

instance FromJSON ModelItem where
  parseJSON = withObject "ModelItem" $ \o ->
    ModelItem
      <$> o .: "title"
      <*> o .: "summary"
      <*> o .:? "subjects" .!= []

instance FromJSON DiscoveredContent where
  parseJSON = withObject "DiscoveredContent" $ \o ->
    DiscoveredContent
      <$> o .: "title"
      <*> o .: "url"
      <*> o .: "summary"
      <*> o .:? "subjects" .!= []

instance ToJSON DiscoveredContent where
  toJSON DiscoveredContent {..} =
    object
      [ "title" .= dcTitle,
        "url" .= dcUrl,
        "summary" .= dcSummary,
        "subjects" .= dcSubjects
      ]

-- | A post draft generated by the AI from one or more content sources.
data GeneratedDraft = GeneratedDraft
  { gdTitle :: !Text,
    -- | Git branch name to push the draft to, e.g. @"draft/my-post-title"@.
    gdBranch :: !Text,
    -- | English Markdown body (without the leading TAGS line; Hugo front-matter
    -- is added separately by 'renderHugoPost').
    gdBodyEn :: !Text,
    -- | Brazilian Portuguese Markdown body.
    gdBodyPtBr :: !Text,
    -- | Tags extracted from the leading @TAGS:@ line of the AI output.
    gdTags :: ![Text],
    -- | Total tokens consumed by this generation call
    --   (@usageMetadata.totalTokenCount@ in the Gemini response).
    gdTokensUsed :: !Int
  }
  deriving (Show, Eq, Generic)

instance FromJSON GeneratedDraft where
  parseJSON = withObject "GeneratedDraft" $ \o ->
    GeneratedDraft
      <$> o .: "gdTitle"
      <*> o .: "gdBranch"
      <*> o .: "gdBodyEn"
      <*> o .: "gdBodyPtBr"
      <*> (o .:? "gdTags" .!= [])
      <*> (o .:? "gdTokensUsed" .!= 0)

instance ToJSON GeneratedDraft where
  toJSON GeneratedDraft {..} =
    object
      [ "gdTitle" .= gdTitle,
        "gdBranch" .= gdBranch,
        "gdBodyEn" .= gdBodyEn,
        "gdBodyPtBr" .= gdBodyPtBr,
        "gdTags" .= gdTags,
        "gdTokensUsed" .= gdTokensUsed
      ]

