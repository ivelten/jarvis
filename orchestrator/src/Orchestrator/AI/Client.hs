-- | Build the @generateContent@ endpoint URL for a specific model.
generateContentUrl :: AiConfig -> Text -> String
generateContentUrl AiConfig {..} model =
  "https://generativelanguage.googleapis.com/v1beta/models/"
    <> T.unpack model
    <> ":generateContent?key="
    <> T.unpack aiApiKey

-- | POST a JSON body to the Gemini @generateContent@ endpoint for a specific
-- model and return the raw response bytes.
postGemini :: AiConfig -> Text -> Value -> IO LBS.ByteString
postGemini cfg model body = do
  initReq <- parseRequest (generateContentUrl cfg model)
  let req =
        initReq
          { method = "POST",
            requestBody = RequestBodyLBS (encode body),
            requestHeaders = [("Content-Type", "application/json")],
            responseTimeout = responseTimeoutMicro (180 * 1_000_000)
          }
  responseBody <$> httpLbs req (aiManager cfg)

-- | Returns 'True' when the exception indicates a Gemini rate-limit error
-- (HTTP 429 or RESOURCE_EXHAUSTED).  Used by 'tryModelsInOrder' to decide
-- whether to fall through to the next model.
isRateLimitError :: SomeException -> Bool
isRateLimitError ex =
  let msg = displayException ex
   in "429" `isInfixOf` msg || "RESOURCE_EXHAUSTED" `isInfixOf` msg

-- | Returns 'True' for any transient Gemini error that warrants falling
-- through to the next model: rate limits and empty responses.
-- An empty response (finishReason=STOP with no text) can occur when a
-- model does not fully support grounded search; a more capable model in
-- the priority list should succeed.
isFallbackError :: SomeException -> Bool
isFallbackError ex =
  isRateLimitError ex
    || "could not extract text from response" `isInfixOf` displayException ex

-- | Try each model from the priority list in order, falling through to the
-- next model on any transient error detected by 'isFallbackError'
-- (rate limits and empty/unsupported responses).
-- Raises an 'IOError' when all models are exhausted or a non-transient error
-- occurs.
tryModelsInOrder :: [Text] -> (Text -> IO a) -> IO a
tryModelsInOrder [] _ = ioError (userError "Gemini: all configured models failed or are rate-limited")
tryModelsInOrder (model : rest) action = do
  result <- try (action model)
  case result of
    Right v -> pure v
    Left ex
      | isFallbackError ex -> do
          putStrLn $ "[AI] Model " <> T.unpack model <> " unavailable (" <> displayException (ex :: SomeException) <> "), trying next..."
          tryModelsInOrder rest action
      | otherwise -> throwIO (ex :: SomeException)

-- | Call Gemini with a specific model and return the extracted text and total
-- token count, raising an 'IOError' on HTTP errors, API error responses, or
-- missing content.
callGeminiWith :: AiConfig -> Text -> Value -> IO (Text, Int)
callGeminiWith cfg model body = do
  respBody <- postGemini cfg model body
  case eitherDecode respBody of
    Left err -> ioError (userError $ "Gemini HTTP response parse error: " <> err)
    Right v -> do
      checkGeminiError v
      case extractText v of
        Nothing ->
          ioError . userError $
            "Gemini: could not extract text from response"
              <> maybe "" (\r -> " (finishReason=" <> T.unpack r <> ")") (extractFinishReason v)
        Just txt -> pure (txt, extractTokenCount v)

-- | POST a request body to Gemini, trying models from 'aiModels' in priority
-- order.  On a rate-limit error the next model is attempted automatically.
-- Raises an 'IOError' when all models are exhausted or a non-rate-limit error
-- occurs.
callGemini :: AiConfig -> Value -> IO (Text, Int)
callGemini cfg body =
  tryModelsInOrder (aiModels cfg) $ \model ->
    callGeminiWith cfg model body

-- | Build a @contents@ array with a single user turn.
userContents :: Text -> Value
userContents prompt =
  object
    [ "role" .= ("user" :: Text),
      "parts" .= [object ["text" .= prompt]]
    ]

-- | Extract @usageMetadata.totalTokenCount@ from a Gemini response.
-- Returns 0 when the field is absent (e.g. in grounded-search responses).
extractTokenCount :: Value -> Int
extractTokenCount = fromMaybe 0 . parseMaybe go
  where
    go = withObject "GeminiResponse" $ \o -> do
      usage <- o .: "usageMetadata"
      usage .: "totalTokenCount"

-- | Extract the text payload from a Gemini response.
--
-- Concatenates all non-thought text parts from the first candidate.
-- Gemini 2.5 thinking models prepend internal-reasoning parts tagged with
-- @\"thought\": true@ before the actual response; those are skipped.
-- When the candidate has no @content@ (e.g. @finishReason@ is @RECITATION@
-- or @SAFETY@) the function returns 'Nothing'.
extractText :: Value -> Maybe Text
extractText = parseMaybe go
  where
    go = withObject "GeminiResponse" $ \o -> do
      candidates <- o .: "candidates"
      case candidates of
        [] -> fail "no candidates in Gemini response"
        (c : _) -> do
          content <- c .: "content"
          parts <- content .: "parts"
          let textParts = mapMaybe (parseMaybe responseTextPart) parts
          case textParts of
            [] -> fail "no text parts in Gemini candidate"
            ts -> pure (T.concat ts)
    responseTextPart = withObject "part" $ \p -> do
      isThought <- p .:? "thought" .!= False
      if isThought
        then fail "thought part"
        else p .: "text"

-- | Extract the @finishReason@ of the first candidate, if present.
extractFinishReason :: Value -> Maybe Text
extractFinishReason = parseMaybe go
  where
    go = withObject "GeminiResponse" $ \o -> do
      candidates <- o .: "candidates"
      case candidates of
        [] -> fail "no candidates"
        (c : _) -> c .: "finishReason"

-- | Decode a UTF-8 text value as JSON, returning an @IO@ error on failure.
decodeText :: (FromJSON a) => Text -> IO a
decodeText t =
  case eitherDecode (LBS.fromStrict (TE.encodeUtf8 t)) of
    Left err -> ioError (userError $ "JSON decode error: " <> err)
    Right v -> pure v

-- | If the Gemini response contains a top-level @{"error": ...}@ object,
-- throw an 'IOError' with the API error code and message so callers get
-- a useful diagnosis (e.g. 429 quota exhausted) instead of a generic
-- "could not extract text" failure.
checkGeminiError :: Value -> IO ()
checkGeminiError v =
  case parseMaybe extractError v of
    Nothing -> pure ()
    Just msg -> ioError (userError $ "Gemini API error: " <> T.unpack msg)
  where
    extractError = withObject "GeminiResponse" $ \o -> do
      errObj <- o .: "error"
      code <- errObj .: "code" :: Parser Int
      msg <- errObj .: "message"
      pure $ "HTTP " <> T.pack (show code) <> " — " <> msg

-- | Strip markdown code fences that Gemini sometimes wraps around JSON output.
-- Handles both @```json\n...\n```@ and bare @```\n...\n```@.
stripFences :: Text -> Text
stripFences t =
  let ls = T.lines t
      trimmed = case ls of
        (h : rest)
          | isJust (T.stripPrefix "```" h) ->
              case reverse rest of
                ("```" : body) -> reverse body
                _ -> rest
        _ -> ls
   in T.strip (T.unlines trimmed)

