-- ---------------------------------------------------------------------------
-- Types
-- ---------------------------------------------------------------------------

-- | Runtime configuration for the Gemini client.
data AiConfig = AiConfig
  { -- | Google AI API key (from environment, see https://aistudio.google.com/apikey).
    aiApiKey :: !Text,
    -- | Model identifier, e.g. @"gemini-2.0-flash"@ or @"gemini-2.5-pro"@.
    aiModel :: !Text,
    aiManager :: !Manager
  }

-- | A piece of web content discovered by the AI.
data DiscoveredContent = DiscoveredContent
  { dcTitle :: !Text,
    dcUrl :: !Text,
    dcSummary :: !Text,
    -- | Optional subject name hint; matched against the Subject table at ingest time.
    dcSubject :: !(Maybe Text)
  }
  deriving (Show, Eq, Generic)

instance FromJSON DiscoveredContent where
  parseJSON = withObject "DiscoveredContent" $ \o ->
    DiscoveredContent
      <$> o .: "title"
      <*> o .: "url"
      <*> o .: "summary"
      <*> o .:? "subject"

instance ToJSON DiscoveredContent where
  toJSON DiscoveredContent {..} =
    object
      [ "title" .= dcTitle,
        "url" .= dcUrl,
        "summary" .= dcSummary,
        "subject" .= dcSubject
      ]

-- | A post draft generated by the AI from one or more content sources.
data GeneratedDraft = GeneratedDraft
  { gdTitle :: !Text,
    -- | Git branch name to push the draft to, e.g. @"draft/my-post-title"@.
    gdBranch :: !Text,
    -- | Markdown body (includes Hugo front-matter if requested).
    gdBody :: !Text
  }
  deriving (Show, Eq, Generic)

instance FromJSON GeneratedDraft

instance ToJSON GeneratedDraft

-- ---------------------------------------------------------------------------
-- Embedded prompts
-- ---------------------------------------------------------------------------

-- | Prompt for 'discoverContent' – embedded from @prompts/discover_content.txt@.
discoverPrompt :: Text
discoverPrompt = T.pack $(makeRelativeToProject "prompts/discover_content.txt" >>= embedStringFile)

-- | Prompt template for 'generateDraft' – embedded from @prompts/generate_draft.txt@.
-- The literal @{{SOURCES}}@ is replaced at runtime with the formatted sources list.
draftPromptTemplate :: Text
draftPromptTemplate = T.pack $(makeRelativeToProject "prompts/generate_draft.txt" >>= embedStringFile)

-- ---------------------------------------------------------------------------
-- Internal HTTP helpers
-- ---------------------------------------------------------------------------

-- | Build the @generateContent@ endpoint URL for the configured model.
generateContentUrl :: AiConfig -> String
generateContentUrl AiConfig {..} =
  "https://generativelanguage.googleapis.com/v1beta/models/"
    <> T.unpack aiModel
    <> ":generateContent?key="
    <> T.unpack aiApiKey

-- | POST a JSON body to the Gemini @generateContent@ endpoint and return the
--   raw response bytes.
postGemini :: AiConfig -> Value -> IO LBS.ByteString
postGemini cfg body = do
  initReq <- parseRequest (generateContentUrl cfg)
  let req =
        initReq
          { method = "POST",
            requestBody = RequestBodyLBS (encode body),
            requestHeaders = [("Content-Type", "application/json")]
          }
  responseBody <$> httpLbs req (aiManager cfg)

-- | Extract the text payload from the first candidate's first part.
--   Gemini wraps generated content in:
--   @candidates[0].content.parts[0].text@
extractText :: Value -> Maybe Text
extractText = parseMaybe go
  where
    go = withObject "GeminiResponse" $ \o -> do
      candidates <- o .: "candidates"
      case candidates of
        [] -> fail "no candidates in Gemini response"
        (c : _) -> do
          content <- c .: "content"
          parts <- content .: "parts"
          case parts of
            [] -> fail "no parts in Gemini candidate"
            (p : _) -> p .: "text"

-- | Decode a UTF-8 text value as JSON, returning an @IO@ error on failure.
decodeText :: (FromJSON a) => Text -> IO a
decodeText t =
  case eitherDecode (LBS.fromStrict (TE.encodeUtf8 t)) of
    Left err -> ioError (userError $ "JSON decode error: " <> err)
    Right v -> pure v

-- ---------------------------------------------------------------------------
-- Public API
-- ---------------------------------------------------------------------------

-- | Ask Gemini (with Google Search grounding) to discover recent, relevant
--   content and return it as a structured list.
discoverContent :: AiConfig -> IO [DiscoveredContent]
discoverContent cfg = do
  respBody <- postGemini cfg requestBody'
  case eitherDecode respBody of
    Left err -> ioError (userError $ "Gemini HTTP response parse error: " <> err)
    Right v ->
      case extractText v of
        Nothing -> ioError (userError "Gemini: could not extract text from response")
        Just txt -> decodeText txt
  where
    prompt = discoverPrompt
    requestBody' =
      object
        [ "contents"
            .= [ object
                   [ "role" .= ("user" :: Text),
                     "parts" .= [object ["text" .= prompt]]
                   ]
               ],
          "tools" .= [object ["google_search" .= object []]],
          "generationConfig"
            .= object
              [ "responseMimeType" .= ("application/json" :: Text),
                "responseSchema"
                  .= object
                    [ "type" .= ("array" :: Text),
                      "items"
                        .= object
                          [ "type" .= ("object" :: Text),
                            "properties"
                              .= object
                                [ "title" .= object ["type" .= ("string" :: Text)],
                                  "url" .= object ["type" .= ("string" :: Text)],
                                  "summary" .= object ["type" .= ("string" :: Text)],
                                  "subject" .= object ["type" .= ("string" :: Text)]
                                ],
                            "required" .= (["title", "url", "summary"] :: [Text])
                          ]
                    ]
              ]
        ]

-- | Ask Gemini to write a full Markdown blog post draft based on the provided
--   content sources.
generateDraft :: AiConfig -> [DiscoveredContent] -> IO GeneratedDraft
generateDraft cfg sources = do
  respBody <- postGemini cfg requestBody'
  case eitherDecode respBody of
    Left err -> ioError (userError $ "Gemini HTTP response parse error: " <> err)
    Right v ->
      case extractText v of
        Nothing -> ioError (userError "Gemini: could not extract text from response")
        Just mdText -> do
          let (title, _rest) = splitTitle mdText
              branch = "draft/" <> toSlug title
          pure
            GeneratedDraft
              { gdTitle = title,
                gdBranch = branch,
                gdBody = mdText
              }
  where
    sourcesBlock =
      T.unlines $
        zipWith formatSource ([1 ..] :: [Int]) sources
    formatSource i dc =
      T.pack (show i)
        <> ". **"
        <> dcTitle dc
        <> "**\n   URL: "
        <> dcUrl dc
        <> "\n   "
        <> dcSummary dc
    prompt = T.replace "{{SOURCES}}" sourcesBlock draftPromptTemplate
    requestBody' =
      object
        [ "contents"
            .= [ object
                   [ "role" .= ("user" :: Text),
                     "parts" .= [object ["text" .= prompt]]
                   ]
               ],
          "generationConfig"
            .= object
              [ "temperature" .= (0.7 :: Double),
                "maxOutputTokens" .= (2048 :: Int)
              ]
        ]

-- ---------------------------------------------------------------------------
-- Utilities
-- ---------------------------------------------------------------------------

-- | Split the first H1 heading from the document body.
splitTitle :: Text -> (Text, Text)
splitTitle txt =
  case T.lines txt of
    (h : rest) -> (T.strip (T.dropWhile (== '#') h), T.unlines rest)
    [] -> ("Untitled", txt)

-- | Convert a title into a URL-safe slug.
toSlug :: Text -> Text
toSlug =
  T.intercalate "-"
    . filter (not . T.null)
    . T.splitOn " "
    . T.map (\c -> if isAlphaNum c || c == ' ' then toLower c else ' ')
