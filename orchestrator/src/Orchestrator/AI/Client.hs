-- | Runtime configuration for the Gemini client.
data AiConfig = AiConfig
  { -- | Google AI API key (from environment, see https://aistudio.google.com/apikey).
    aiApiKey :: !Text,
    -- | Ordered list of model identifiers to try, highest-priority first.
    -- When a model returns a rate-limit error (HTTP 429 / RESOURCE_EXHAUSTED)
    -- the next model in the list is attempted automatically.
    aiModels :: ![Text],
    aiManager :: !Manager
  }

-- | A post draft generated by the AI from one or more content sources.
data GeneratedDraft = GeneratedDraft
  { gdTitle :: !Text,
    -- | Git branch name to push the draft to, e.g. @"draft/my-post-title"@.
    gdBranch :: !Text,
    -- | English Markdown body (without the leading TAGS line; Hugo front-matter
    -- is added separately by 'renderHugoPost').
    gdBodyEn :: !Text,
    -- | Brazilian Portuguese Markdown body.
    gdBodyPtBr :: !Text,
    -- | Tags extracted from the leading @TAGS:@ line of the AI output.
    gdTags :: ![Text],
    -- | Total tokens consumed by this generation call
    --   (@usageMetadata.totalTokenCount@ in the Gemini response).
    gdTokensUsed :: !Int
  }
  deriving (Show, Eq, Generic)

instance FromJSON GeneratedDraft where
  parseJSON = withObject "GeneratedDraft" $ \o ->
    GeneratedDraft
      <$> o .: "gdTitle"
      <*> o .: "gdBranch"
      <*> o .: "gdBodyEn"
      <*> o .: "gdBodyPtBr"
      <*> (o .:? "gdTags" .!= [])
      <*> (o .:? "gdTokensUsed" .!= 0)

instance ToJSON GeneratedDraft where
  toJSON GeneratedDraft {..} =
    object
      [ "gdTitle" .= gdTitle,
        "gdBranch" .= gdBranch,
        "gdBodyEn" .= gdBodyEn,
        "gdBodyPtBr" .= gdBodyPtBr,
        "gdTags" .= gdTags,
        "gdTokensUsed" .= gdTokensUsed
      ]

